-- ─────────────────────────────────────────────────────────────────────
-- STEP 1. 데이터베이스 생성 및 선택
-- ─────────────────────────────────────────────────────────────────────

-- 데이터베이스가 없을 때만 새로 만듭니다 (이미 있으면 무시).
-- CHARACTER SET utf8mb4 : 한글과 이모지(😀)까지 안전하게 저장할 수 있는 인코딩입니다.
-- COLLATE utf8mb4_unicode_ci : 대소문자를 구분하지 않고 한글 정렬을 올바르게 처리합니다.
CREATE DATABASE IF NOT EXISTS job_data_platform
    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;

-- 이후 SQL 명령이 어느 데이터베이스를 대상으로 실행할지 지정합니다.
USE job_data_platform;


-- ─────────────────────────────────────────────────────────────────────
-- STEP 2. jobs 테이블 생성
-- ─────────────────────────────────────────────────────────────────────
-- 이 테이블은 사람인에서 크롤링한 채용공고 데이터를 저장합니다.
-- IF NOT EXISTS : 테이블이 이미 존재하면 아무 것도 하지 않고 넘어갑니다.
--                 schema.sql을 여러 번 실행해도 기존 데이터는 유지됩니다.
-- ─────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS jobs (

    -- ┌─────────────────────────────────────────────────────────────┐
    -- │  컬럼 정의                                                   │
    -- └─────────────────────────────────────────────────────────────┘

    -- [id] 각 공고 행(row)을 식별하는 고유 번호
    --   INT            : 정수형 숫자 타입 (1, 2, 3 ...)
    --   AUTO_INCREMENT : INSERT 할 때마다 숫자를 자동으로 1씩 증가시킵니다.
    --                    직접 넣을 필요 없이 DB가 알아서 부여합니다.
    --   PRIMARY KEY    : 이 테이블에서 각 행을 유일하게 구별하는 기본 키입니다.
    --                    NULL 불가 + 중복 불가가 자동으로 보장됩니다.
    id          INT           AUTO_INCREMENT PRIMARY KEY,

    -- [company] 채용공고를 올린 회사명
    --   VARCHAR(255) : 최대 255자까지 저장할 수 있는 가변 길이 문자열 타입입니다.
    --                  실제 저장된 글자 수만큼만 공간을 사용하므로 효율적입니다.
    --   NOT NULL     : 반드시 값이 있어야 합니다. 빈 값(NULL)은 허용하지 않습니다.
    company     VARCHAR(255)  NOT NULL,

    -- [title] 채용공고 제목 (예: "Python 백엔드 개발자 채용")
    --   VARCHAR(500) : 공고 제목은 회사명보다 길 수 있어 500자로 설정합니다.
    --   NOT NULL     : 제목 없는 공고는 저장하지 않습니다.
    title       VARCHAR(500)  NOT NULL,

    -- [link] 채용공고 원문 URL (예: https://www.saramin.co.kr/...)
    --   VARCHAR(1000) : URL은 파라미터까지 포함되면 길어질 수 있어 1000자로 설정합니다.
    --   NOT NULL      : 링크 없는 공고는 저장하지 않습니다.
    link        VARCHAR(1000) NOT NULL,

    -- [created_at] 이 공고가 DB에 저장된 날짜와 시각
    --   TIMESTAMP              : 날짜+시각을 함께 저장하는 타입 (예: 2026-04-04 23:15:00)
    --   DEFAULT CURRENT_TIMESTAMP : INSERT 시 자동으로 현재 시각이 기록됩니다.
    --                               Python 코드에서 별도로 값을 넣을 필요가 없습니다.
    --
    --   💡 활용 예시:
    --      - API에서 ORDER BY created_at DESC 로 최신 공고를 상단에 표시
    --      - 크롤러가 언제 실행되었는지 이력 추적
    created_at  TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,

    -- ┌─────────────────────────────────────────────────────────────┐
    -- │  제약 조건 (Constraints)                                     │
    -- └─────────────────────────────────────────────────────────────┘

    -- [UNIQUE KEY uq_link] link 컬럼에 대한 중복 방지 제약
    --
    --   UNIQUE KEY : 이 키가 걸린 컬럼에는 동일한 값을 가진 행이
    --                2개 이상 존재할 수 없도록 DB 자체가 강제합니다.
    --
    --   왜 link 컬럼에 거는가?
    --     - 같은 채용공고 URL은 세상에 단 하나이므로, link가 같으면 = 같은 공고입니다.
    --     - 크롤러를 매일 실행해도 동일한 공고가 중복 저장되는 것을 DB 레벨에서 막습니다.
    --     - Python 코드의 중복 제거 로직(if job_info not in jobs_list)은
    --       '같은 실행 안에서'만 동작하지만, UNIQUE KEY는 '다른 날 실행해도' 막아줍니다.
    --
    --   INSERT IGNORE 와의 조합:
    --     - INSERT IGNORE 를 쓰면 UNIQUE 제약에 걸렸을 때 에러 없이 조용히 스킵합니다.
    --     - UNIQUE KEY + INSERT IGNORE = 실무에서 자주 쓰는 중복 방지 정석 패턴
    UNIQUE KEY uq_link (link)

);


